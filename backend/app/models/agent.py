class AgentExecution(UUIDPrimaryKeyMixin, TimestampMixin, Base):
    """
    Execution record for a single agent invocation within an ``AgentTask``.

    One ``AgentExecution`` is created for each node execution in the LangGraph
    workflow.  This provides granular tracing of the multi-agent timeline:
    - Which agent ran
    - What inputs it received
    - What it produced
    - How long it took
    - How many tokens it consumed
    - What quality score it received from the Reflection Agent

    Retry attempts create new ``AgentExecution`` records (with ``retry_of``
    pointing to the original) rather than mutating the failed record.
    """

    __tablename__ = "agent_executions"
    __table_args__ = (
        Index("ix_agent_executions_task_id", "task_id"),
        Index("ix_agent_executions_agent_type", "agent_type"),
        Index("ix_agent_executions_status", "status"),
        Index("ix_agent_executions_started_at", "started_at"),
        Index("ix_agent_executions_step_index", "step_index"),
        {"comment": "Per-agent execution traces within a task workflow."},
    )

    task_id: Mapped[uuid.UUID] = mapped_column(
        PG_UUID(as_uuid=True),
        ForeignKey("agent_tasks.id", ondelete="CASCADE"),
        nullable=False,
    )
    retry_of: Mapped[uuid.UUID | None] = mapped_column(
        PG_UUID(as_uuid=True),
        ForeignKey("agent_executions.id", ondelete="SET NULL"),
        nullable=True,
        doc="If this is a retry, points to the original failed execution.",
    )
    step_index: Mapped[int] = mapped_column(
        Integer,
        nullable=False,
        doc="Zero-based step index within the task's execution plan.",
    )
    step_name: Mapped[str | None] = mapped_column(
        String(200),
        nullable=True,
        doc="Human-readable step name from the execution plan.",
    )
    agent_type: Mapped[AgentType] = mapped_column(
        SAEnum(AgentType, name="agent_type_enum", create_type=True),
        nullable=False,
    )
    agent_instance_id: Mapped[str | None] = mapped_column(
        String(255),
        nullable=True,
        doc="Unique instance ID for this agent invocation (for log correlation).",
    )
    status: Mapped[AgentStatus] = mapped_column(
        SAEnum(AgentStatus, name="agent_status_enum", create_type=True),
        nullable=False,
        default=AgentStatus.IDLE,
        server_default=text("'idle'"),
    )
    # Timing
    started_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
    )
    completed_at: Mapped[datetime | None] = mapped_column(
        DateTime(timezone=True),
        nullable=True,
    )
    duration_ms: Mapped[int | None] = mapped_column(
        Integer,
        nullable=True,
    )
    # Model used
    model_id: Mapped[str | None] = mapped_column(
        String(100),
        nullable=True,
        doc="Model identifier used by this agent (e.g. 'llama3.1:70b').",
    )
    prompt_version: Mapped[str | None] = mapped_column(
        String(50),
        nullable=True,
        doc="Prompt template version used (e.g. 'v2').",
    )
    # Token accounting
    prompt_tokens: Mapped[int | None] = mapped_column(Integer, nullable=True)
    completion_tokens: Mapped[int | None] = mapped_column(Integer, nullable=True)
    total_tokens: Mapped[int | None] = mapped_column(Integer, nullable=True)
    # Inputs and outputs
    input_data: Mapped[dict | None] = mapped_column(
        JSONB,
        nullable=True,
        doc="Serialised input context passed to this agent.",
    )
    output_data: Mapped[dict | None] = mapped_column(
        JSONB,
        nullable=True,
        doc="Serialised agent output.",
    )
    output_text: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="Raw text output (for assistant / research agents).",
    )
    # Quality signals
    reflection_score: Mapped[float | None] = mapped_column(
        Float,
        nullable=True,
        doc="Score from the Reflection Agent (0.0–1.0).",
    )
    reflection_critique: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="Critique text from the Reflection Agent.",
    )
    confidence_score: Mapped[float | None] = mapped_column(
        Float,
        nullable=True,
    )
    debate_triggered: Mapped[bool] = mapped_column(
        Boolean,
        nullable=False,
        default=False,
        server_default=text("false"),
        doc="True if this execution's output triggered the Debate Agent.",
    )
    # Error tracking
    retry_count: Mapped[int] = mapped_column(
        Integer,
        nullable=False,
        default=0,
        server_default=text("0"),
    )
    error_message: Mapped[str | None] = mapped_column(Text, nullable=True)
    error_code: Mapped[str | None] = mapped_column(String(100), nullable=True)
    error_traceback: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
        doc="Full traceback for FAILED executions (stored for debugging).",
    )
    # Retrieval context (for research / RAG agents)
    retrieved_chunk_ids: Mapped[list[str]] = mapped_column(
        ARRAY(String),
        nullable=False,
        default=list,
        server_default=text("'{}'"),
        doc="Qdrant point IDs of retrieved chunks used by this agent.",
    )
    retrieval_score_avg: Mapped[float | None] = mapped_column(
        Float,
        nullable=True,
        doc="Average cross-encoder score of retrieved chunks.",
    )
    extra_data: Mapped[dict] = mapped_column(
        JSONB,
        nullable=False,
        default=dict,
        server_default=text("'{}'"),
    )

    # ------------------------------------------------------------------
    # Relationships
    # ------------------------------------------------------------------
    task: Mapped["AgentTask"] = relationship(
        "AgentTask",
        back_populates="executions",
    )
    retried_from: Mapped["AgentExecution | None"] = relationship(
        "AgentExecution",
        foreign_keys=[retry_of],
        remote_side="AgentExecution.id",
        doc="The execution that this is a retry of.",
    )
    tool_executions: Mapped[list["ToolExecution"]] = relationship(
        "ToolExecution",
        back_populates="agent_execution",
        cascade="all, delete-orphan",
        passive_deletes=True,
        doc="Tool calls made during this agent execution.",
    )

    def __repr__(self) -> str:
        return (
            f"<AgentExecution id={self.id} agent={self.agent_type} "
            f"step={self.step_index} status={self.status} "
            f"duration={self.duration_ms}ms>"
        )