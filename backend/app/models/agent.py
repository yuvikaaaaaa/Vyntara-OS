"""
Intelligence Operating System — Agent Models
=============================================
ORM models for the agent orchestration domain:

``AgentTask``       — Top-level task submitted by a user. Drives the
                      LangGraph workflow from planning through to completion.
``AgentExecution``  — Per-agent execution record within a task. One row per
                      agent invocation; traces the complete multi-agent timeline.

Hierarchy:
    User  →  AgentTask  →  AgentExecution (many)

An ``AgentTask`` owns the high-level lifecycle and plan.
Each ``AgentExecution`` records what one specific agent did during that task —
its inputs, outputs, token usage, quality scores, and error details.

This separation enables:
- Task-level rollup metrics (total tokens, duration, success)
- Agent-level granular tracing (per-agent latency, quality)
- Replay: re-run individual agent steps with modified inputs
- Evaluation: compare agent quality across prompt versions

Cascade policy:
    AgentTask      → User         (cascade delete)
    AgentTask      → Conversation (SET NULL — task survives conversation deletion)
    AgentExecution → AgentTask    (cascade delete)
"""

from __future__ import annotations

import uuid
from datetime import datetime
from typing import TYPE_CHECKING

from sqlalchemy import (
    Boolean,
    DateTime,
    Enum as SAEnum,
    Float,
    ForeignKey,
    Index,
    Integer,
    String,
    Text,
    text,
)
from sqlalchemy.dialects.postgresql import ARRAY, JSONB
from sqlalchemy.dialects.postgresql import UUID as PG_UUID
from sqlalchemy.orm import Mapped, mapped_column, relationship

from app.core.enums import AgentStatus, AgentType, TaskPriority, TaskStatus, WorkflowType
from app.models.base import AuditMixin, Base, TimestampMixin, UUIDPrimaryKeyMixin

if TYPE_CHECKING:
    from app.models.conversation import Conversation
    from app.models.evaluation import Evaluation
    from app.models.memory import EpisodicMemory
    from app.models.tool import ToolExecution
    from app.models.user import User
    from app.models.workflow import Workflow


